import Foundation

print("String Literals")


//  Escape sequence	Meaning

//  \0	Null Character
//  \\	\character
//  \b	Backspace
//  \f	Form feed
//  \n	Newline
//  \r	Carriage return
//    \t	Horizontal tab
//  \v	Vertical tab
//  \'	Single Quote
//  \"	Double Quote
//  \000	Octal number of one to three digits
//  \xhh...	Hexadecimal number of one or more digits


let stringL = " Hello \r \t World \n \n Hello \' Swift \'  "
print(stringL)

